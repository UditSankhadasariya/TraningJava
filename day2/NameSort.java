package day2;

import java.util.Arrays;

public class NameSort {
	public static void main(String[] args) {
		String [] names = {"udit","abasd","zjfp","fds"};
		
		for(String temp :names) {
			System.out.println(temp);
		}
		
		Arrays.sort(names);
		
		
		
		for(String temp :names) {
			System.out.println(temp);
		}
	}

}
